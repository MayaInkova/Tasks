package ObjectAndClass;
//програма, която генерира случайно рекламно съобщение за някакъв продукт. Съобщенията трябва да се състоят от фраза, следвани от  случка,
// следвани от автор (първо и второ име) и град, които се избират от предварително подготвени списъци. Например, нека имаме следните списъци:
// ФРАЗА:  {"Продуктът е отличен.", "Това е страхотен продукт.", "Постоянно ползвам този продукт.", "Това е най-добрият продукт от тази категория."}.
// случки: {"Вече се чувствам добре.", "Успях да се променя.", "Той направи чудо.", "Не мога да повярвам, но вече се чувствам страхотно.", "Опитайте и вие. Аз съм много доволна."}.
// Първо име на автор: {"Диана", "Петя", "Стела", "Елена", "Катя"}.
//Второ име на автор: {"Иванова", "Петрова", "Кирова"}.
//  Градове: {"София", "Пловдив", "Варна", "Русе", "Бургас"}.

import java.util.Random;

public class AdvertisementGenerator {
    public static void main(String[] args) {
        // Списъци с хвалебствени фрази, случки, имена и градове
        String[] phrases = {
                "Продуктът е отличен.",
                "Това е страхотен продукт.",
                "Постоянно ползвам този продукт.",
                "Това е най-добрият продукт от тази категория."
        };

        String[] events = {
                "Вече се чувствам добре.",
                "Успях да се променя.",
                "Той направи чудо.",
                "Не мога да повярвам, но вече се чувствам страхотно.",
                "Опитайте и вие. Аз съм много доволна."
        };

        String[] firstNames = {
                "Диана",
                "Петя",
                "Стела",
                "Елена",
                "Катя"
        };

        String[] lastNames = {
                "Иванова",
                "Петрова",
                "Кирова"
        };

        String[] cities = {
                "София",
                "Пловдив",
                "Варна",
                "Русе",
                "Бургас"
        };

        // Генератор на случайни числа
        Random random = new Random();

        // Случайно избиране на елементи от всеки списък
        String randomPhrase = phrases[random.nextInt(phrases.length)];
        String randomEvent = events[random.nextInt(events.length)];
        String randomFirstName = firstNames[random.nextInt(firstNames.length)];
        String randomLastName = lastNames[random.nextInt(lastNames.length)];
        String randomCity = cities[random.nextInt(cities.length)];

        // Генериране на рекламното съобщение
        String advertisement = randomPhrase + " " + randomEvent + " - " + randomFirstName + " " + randomLastName + ", " + randomCity + ".";

        // Извеждане на рекламното съобщение
        System.out.println(advertisement);
    }
}
